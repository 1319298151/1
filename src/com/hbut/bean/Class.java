package com.hbut.bean;

public class Class {
	
	private String classNo;
	private int pepopleNum;
	private int grade;
	private String major;
	private int index;
	
	public String getClassNo() {
		return classNo;
	}
	public void setClassNo(String classNo) {
		this.classNo = classNo;
	}
	public int getPepopleNum() {
		return pepopleNum;
	}
	public void setPepopleNum(int pepopleNum) {
		this.pepopleNum = pepopleNum;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	
}
