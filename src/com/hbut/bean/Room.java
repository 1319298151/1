package com.hbut.bean;

public class Room {
	
	private String RoomNO;
	private int seatNum;
	
	public String getRoomNO() {
		return RoomNO;
	}
	public void setRoomNO(String roomNO) {
		RoomNO = roomNO;
	}
	public int getSeatNum() {
		return seatNum;
	}
	public void setSeatNum(int seatNum) {
		this.seatNum = seatNum;
	}
	
}
